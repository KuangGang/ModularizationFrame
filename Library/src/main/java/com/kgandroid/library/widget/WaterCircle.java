package com.kgandroid.library.widget;

/**
 * 进度条背景实心圆
 * @author JiaWei
 *
 */
public class WaterCircle {

	private float x;// 圆X坐标
	private float y;// 圆Y坐标
	private float radius;// 圆半径
	private int color;// 圆的颜色
	public float getX() {
		return x;
	}
	public void setX(float x) {
		this.x = x;
	}
	public float getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}
	public float getRadius() {
		return radius;
	}
	public void setRadius(float radius) {
		this.radius = radius;
	}
	public int getColor() {
		return color;
	}
	public void setColor(int color) {
		this.color = color;
	}
	
	

}
